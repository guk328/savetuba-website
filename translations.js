function setLanguage(lang) {
  if (!translations[lang]) return;

  // Text content elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key] !== undefined) {
      el.textContent = translations[lang][key];
    }
  });

  // Image src elements
  document.querySelectorAll('[data-i18n-src]').forEach(el => {
    const key = el.getAttribute('data-i18n-src');
    if (translations[lang][key] !== undefined) {
      el.src = translations[lang][key];
    }
  });

  // HTML content elements (contain spans, line breaks, etc.)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (translations[lang][key] !== undefined) {
      el.innerHTML = translations[lang][key];
    }
  });

  // Update active button state
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // Sync privacy policy tabs if on that page
  const ppTabs = document.querySelectorAll('.lang-tab');
  if (ppTabs.length) {
    const mapLang = { en: 'en', kz: 'kz', ru: 'ru' };
    const ppLang = mapLang[lang] || 'en';
    document.querySelectorAll('.lang-section').forEach(s => s.classList.remove('active'));
    document.querySelectorAll('.lang-tab').forEach(t => t.classList.remove('active'));
    const target = document.getElementById('lang-' + ppLang);
    if (target) target.classList.add('active');
    ppTabs.forEach(t => {
      if (t.getAttribute('onclick')?.includes("'" + ppLang + "'")) t.classList.add('active');
    });
  }

  // Set html lang attribute
  document.documentElement.lang = lang;
  localStorage.setItem('savetuba-lang', lang);
}

function initLanguage() {
  const saved = localStorage.getItem('savetuba-lang') || 'en';
  // Wire up buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => setLanguage(btn.dataset.lang));
  });
  setLanguage(saved);
}

document.addEventListener('DOMContentLoaded', initLanguage);

document.addEventListener('click', function(e) {
  const navLinks = document.querySelector('.nav-links');
  const toggle = document.querySelector('.nav-mobile-toggle');
  if (navLinks && navLinks.classList.contains('open') &&
      !navLinks.contains(e.target) && !toggle.contains(e.target)) {
    navLinks.classList.remove('open');
    toggle.setAttribute('aria-expanded', 'false');
  }
});
